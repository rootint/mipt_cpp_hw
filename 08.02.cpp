#include <cassert>
#include <sstream>
#include <string>

using namespace std::literals;

#include "08.13.hpp"

int main()
{
    Integer x = std::string(32, '1');

    Integer y = std::string(32, '2');

    //  ----------------------------------------------------------------------------------------

    assert((x += y) == "+33333333333333333333333333333333"s);
    assert((x -= y) == "+11111111111111111111111111111111"s);
    assert((x *= y) == "+246913580246913580246913580246908641975308641975308641975308642"s);
    assert((x /= y) == "+11111111111111111111111111111111"s);

    //  ----------------------------------------------------------------------------------------

    assert((x++) == "+11111111111111111111111111111111"s);
    assert((x--) == "+11111111111111111111111111111112"s);
    assert((++y) == "+22222222222222222222222222222223"s);
    assert((--y) == "+22222222222222222222222222222222"s);

    //  ----------------------------------------------------------------------------------------

    assert((x + y) == "+33333333333333333333333333333333"s);
    assert((x - y) == "-11111111111111111111111111111111"s);
    assert((x * y) == "+246913580246913580246913580246908641975308641975308641975308642"s);
    assert((x / y) == "+0"s);

    //  ----------------------------------------------------------------------------------------

    assert((x < y) == 1);
    assert((x > y) == 0);
    assert((x <= y) == 1);
    assert((x >= y) == 0);
    assert((x == y) == 0);
    assert((x != y) == 1);

    //  ----------------------------------------------------------------------------------------

    std::stringstream stream_1(std::string(32, '1'));
    std::stringstream stream_2;

    //  ----------------------------------------------------------------------------------------

    stream_1 >> x;
    stream_2 << x;

    //  ----------------------------------------------------------------------------------------

    assert(stream_2.str() == stream_1.str());

    //  ----------------------------------------------------------------------------------------

    assert(sqrt(multiply(x, x)) == x);

    //  ----------------------------------------------------------------------------------------
    //  New tests for operator%, pow, sign, abs
    //  ----------------------------------------------------------------------------------------

    Integer a("100");
    Integer b("7");

    assert((a % b) == Integer("2"));
    assert((a %= b) == Integer("2"));

    a = Integer("100");

    assert((a % Integer("10")) == Integer("0"));

    //  ----------------------------------------------------------------------------------------

    assert(pow(Integer("2"), 10) == Integer("1024"));
    assert(pow(Integer("3"), 0) == Integer("1"));
    assert(pow(Integer("5"), 3) == Integer("125"));

    assert(pow(Integer("-2"), 3) == Integer("-8"));
    assert(pow(Integer("-2"), 4) == Integer("16"));

    //  ----------------------------------------------------------------------------------------

    assert(Integer("42").sign() == 1);
    assert(Integer("-42").sign() == -1);
    assert(Integer("0").sign() == 0);

    //  ----------------------------------------------------------------------------------------

    assert(Integer("42").abs() == Integer("42"));
    assert(Integer("-42").abs() == Integer("42"));
    assert(Integer("0").abs() == Integer("0"));

    return 0;
}

// All tests pass.